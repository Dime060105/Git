names = ["Dio", "Jotaro", "Zeppeli", "Sun Tzu", "Vergil"] # "[]" rappresentano una lista
names[0] = "Pio" # cambio solo l'elemento con index 0
print(names[0]) # quello tra quadre è l'index dell'elemento, "0" è il primo "1" il secondo e così via, "-1" è l'ultimo
print(names[-1])
print(names[-2]) # il secondo dalla fine
print(names[0:3]) # dall'index 0 al 3
print(names) # tutti i comandi precedenti non modificano la lista definitivamente